package com.joe.springracing.objects;

public class Odds {

	private double win;
	private double place;
	
	public Odds() {
	}

	public double getWin() {
		return win;
	}

	public void setWin(double win) {
		this.win = win;
	}

	public double getPlace() {
		return place;
	}

	public void setPlace(double place) {
		this.place = place;
	}
	

}
